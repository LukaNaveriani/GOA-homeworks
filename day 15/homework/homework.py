# #GPU, CPU და RAM ტექნიკური კომპონენტები არიან, რომლებიც კომპიუტერის მუშაობას მართავენ და სხვადასხვა ფუნქციებს ასრულებენ. თითოეული მათგანი თავისი უნიკალური როლის მიცემით ამყარებს კომპიუტერის ოპტიმალურ ფუნქციონირებას.

# ### 1. **CPU (Central Processing Unit) – ცენტრალური პროცესორი**
# CPU არის კომპიუტერის "გონება". ის პასუხისმგებელია ყველა ძირითადი ამოცანის შესრულებაზე, როგორიცაა მონაცემების დამუშავება, ბრძანებების შესრულება და სხვადასხვა პროგრამული მოთხოვნების შესრულება. CPU იყენებს სერიულ პროცედურას, რაც იმას ნიშნავს, რომ ის ხშირად მუშაობს ერთ ამოცანაზე და შემდეგ გადადის სხვა ამოცანაზე.  
# **პასუხისმგებლობები**:
# - პროგრამების და სისტემის ამოცანების შესრულება
# - კომპიუტერის სხვადასხვა ნაწილების სინქრონიზაცია
# - კომპიუტერის საერთო მუშაობის კონტროლი

# ### 2. **GPU (Graphics Processing Unit) – გრაფიკული პროცესორი**
# GPU ძირითადად გამოიყენება გრაფიკის დამუშავებისთვის. ის სპეციალიზირებულია დიდ რაოდენობად მონაცემების პარალელურ დამუშავებაზე, რაც ძალიან მნიშვნელოვანია ისეთი ამოცანებისთვის, როგორიცაა ვიდეოკონტენტების რენდერინგი, გრაფიკული თამაშები და ხელოვნური ინტელექტის (AI) ამოცანები. GPU-ს შეუძლია ერთდროულად ბევრი მონაცემი განახორციელოს, რაც მას იდეალურ გარემოს ქმნის გრაფიკული ინტერფეისების და 3D გამოსახულებების შექმნისთვის.  
# **პასუხისმგებლობები**:
# - გრაფიკული ელემენტების წარმოება (3D გრაფიკა, ვიდეო)
# - ვიდეოგეიმების და მულტიმედია კონტენტის მხარდაჭერა
# - პარალელური მონაცემების დამუშავება (AI, სამეცნიერო სემინარები)

# ### 3. **RAM (Random Access Memory) – შემთხვევითი წვდომის მეხსიერება**
# RAM კომპიუტერის დროებითი მეხსიერებაა, რომელიც ინახავს ინფორმაციას, როდესაც აპლიკაციები ან პროგრამები მუშაობენ. როდესაც პროგრამა გაიხსნება, მისი მონაცემები ჩაიწერება RAM-ში, რაც საშუალებას აძლევს პროცესორს სწრაფად წვდომა მოახდინოს მასზე. თუმცა, როდესაც კომპიუტერი გამორთულია, RAM-ის მონაცემები იკარგება.  
# **პასუხისმგებლობები**:
# - პროგრამების სწრაფად მუშაობა და სწრაფი წვდომა
# - დროებითი მონაცემების შენახვა
# - კომპიუტერის ოპერაციული დატვირთვის მხარდაჭერა

# ### განსხვავებები:
# - **CPU** პასუხისმგებელია კომპიუტერის ძირითადი მოქმედებების კონტროლზე და ამოცანების შესრულებაზე.
# - **GPU** სპეციალიზირებულია გრაფიკის დამუშავებაზე და გამოიყენება პარალელურ ამოცანებში, სადაც საჭიროა დიდი მოცულობის მონაცემების ერთდროული დამუშავება.
# - **RAM** არის დროებითი მეხსიერება, რომელიც უზრუნველყოფს სწრაფად მუშაობას პროგრამებისთვის და მონაცემების დროებით შენახვას.

